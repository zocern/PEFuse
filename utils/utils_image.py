def RGB_to_Y(RGB):

    if RGB.shape[2] != 3:
        raise ValueError("输入图像需要为 RGB 格式，且有 3 个通道")

    Y = 0.299 * RGB[:, :, 0] + 0.587 * RGB[:, :, 1] + 0.114 * RGB[:, :, 2]

    Y = np.clip(Y, 0, 255).astype(np.uint8)
    if Y.ndim == 2:
        Y = np.expand_dims(Y, axis=2)
    return Y

def RGB2YCbCr(RGB):
    """
    将RGB格式转换为YCrCb格式
    用于中间结果的色彩空间转换中,因为此时rgb_image默认size是[B, C, H, W]
    :param RGB: RGB格式的图像数据
    :return: Y, Cr, Cb
    """
    R = RGB[:, 0:1]
    G = RGB[:, 1:2]
    B = RGB[:, 2:3]
    Y = 0.299 * R + 0.587 * G + 0.114 * B
    Cb = (B - Y) * 0.564 + 0.5
    Cr = (R - Y) * 0.713 + 0.5

    Y = Y.clamp(0.0,1.0)
    Cb = Cb.clamp(0.0, 1.0).detach()
    Cr = Cr.clamp(0.0,1.0).detach()

    return Y, Cb, Cr

def YCbCr2RGB(YCbCr):
    """
    将YcrCb格式转换为RGB格式
    :param YCbCr: YCbCr格式的图像数据
    :return: RGB
    """
    B, C, W, H = YCbCr.shape
    im_flat = YCbCr.transpose(1, 3).transpose(1, 2).reshape(-1, 3)
    mat = torch.tensor([[1.0, 1.0, 1.0], [1.403, -0.714, 0.0], [0.0, -0.344, 1.773]]
    ).to(device)
    bias = torch.tensor([0.0 / 255, -0.5, -0.5]).to(device)
    temp = (im_flat + bias).mm(mat).to(device)
    RGB = temp.reshape(B, W, H, C).transpose(1, 3).transpose(2, 3)
    RGB = RGB.clamp(0, 1.0)
    return RGB
    
